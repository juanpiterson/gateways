module.exports = {

  attributes: {

    //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
    //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
    //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
    serialNumber: {
      type: 'string',
      unique: true,
      required: true,
      maxLength: 100
    },

    name: {
      type: 'string',
      maxLength: 100
    },

    ipAddress: {
      type: 'string',
      isIP: true
    },

    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
    //  ╚═╝╩ ╩╚═╝╚═╝═╩╝╚═╝


    //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
    //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
    //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝
    devices: {
      collection: 'peripheralDevice',
      via: 'gateway'
    }

  },

  beforeCreate: async (valuesToSet, proceed) => {
    const maxDevices = sails.config.globals.maxDevicesPerGateway;

    if (valuesToSet.devices.length > maxDevices) {
      return proceed(new Error(`Maximum allowed devices is ${maxDevices}.`));
    }

    return proceed();
  }

};

